-- ============================================================
-- Migration 00015: 관리자 역할 권한 범위 조정
-- ============================================================
-- 관리자는 운영 관리자이지 진료 주체가 아니다.
-- 진료 행위 권한(기록 작성·수정, 담당/본인 기록 조회, 환자 검색)을 회수하고
-- 감사·관리 권한(records:read_all 포함)만 남긴다.
-- 효과: 권한 기반 메뉴에서 관리자에게 "담당 환자" 등 의사용 메뉴가 사라지고,
-- 진료기록 메뉴는 read_all 기반 "전체 진료기록"으로 동작한다.
-- 가산적 데이터 조정 (스키마 변경 없음, 무중단).
-- 주의: 00011의 "관리자 ← 전체 권한" 시드는 이 마이그레이션으로 조정됨.
-- ============================================================

DELETE FROM role_permissions
WHERE role_id = 'a0000000-0000-0000-0000-000000000001'
  AND permission_id IN (
    'b0000000-0000-0000-0000-000000000009',  -- records:create
    'b0000000-0000-0000-0000-000000000010',  -- records:update_own
    'b0000000-0000-0000-0000-000000000011',  -- records:read_own
    'b0000000-0000-0000-0000-000000000012',  -- records:read_assigned
    'b0000000-0000-0000-0000-000000000014'   -- patients:search
  );
